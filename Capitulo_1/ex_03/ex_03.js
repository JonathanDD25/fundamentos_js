// los tipos de dato son las distintas formas o definiciones que se tienen para relacionar datos en las variables, cuando se declara una variable y se inicializa, se le esta asignando un valor y ese valor es un tipo de dato; es decir: entero, decimal, boleano, cadena o caracter entre otros.



